/**
 * Copy gemmimol's gemmi WebAssembly glue (`gemmi.js`) and binary (`gemmi.wasm`)
 * into `public/gemmi/`, so both serve from a stable, base-relative URL
 * (`<base>/gemmi/…`).
 *
 * gemmi.js is loaded at runtime as a *classic script* (see `src/lib/gemmi.ts`):
 * its Emscripten glue locates the `.wasm` relative to its own script URL, which
 * only works when the two sit side by side as static files. Bundling gemmi.js
 * instead breaks that lookup, and importing the `.wasm` via Vite's `?url` fails
 * in dev because the file lives deep in the pnpm store. Copying both into
 * `public/` sidesteps all of it. The copies are gitignored and regenerated by
 * the `dev` / `build` scripts, so they stay in sync with the installed version.
 */
import { copyFile, mkdir } from 'node:fs/promises'
import { createRequire } from 'node:module'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const require = createRequire(import.meta.url)
const root = path.dirname(fileURLToPath(new URL('.', import.meta.url)))

const vendorDir = path.join(
  path.dirname(require.resolve('gemmimol/package.json')),
  'vendor/wasm',
)
const destDir = path.join(root, 'public/gemmi')

await mkdir(destDir, { recursive: true })
for (const file of ['gemmi.js', 'gemmi.wasm']) {
  const dest = path.join(destDir, file)
  await copyFile(path.join(vendorDir, file), dest)
  console.log(`Copied ${file} → ${path.relative(root, dest)}`)
}
